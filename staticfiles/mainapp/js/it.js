const itBtns = document.querySelectorAll('.it-btn');
const itContent = document.getElementById('itContent');

itBtns.forEach(btn => {
    btn.addEventListener('click', function() {
        const tech = this.getAttribute('data-it');
        
        itBtns.forEach(b => b.classList.remove('active'));
        this.classList.add('active');
        
        itContent.innerHTML = itData[tech].content;
    });
});

document.addEventListener('DOMContentLoaded', function() {
    itContent.innerHTML = itData.bim.content;
});